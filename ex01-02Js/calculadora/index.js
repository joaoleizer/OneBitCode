let multiplication = number1 * number2
let division = number1 / number2

alert(
    "Resultados: " +
    "\nSoma: " + sum +
    "\nSubtração: " + subtraction +
    "\nDivisão: " + division +
    "\nMultiplicação: " + multiplication
)
